package org.leeon.pattern.factory.method;
/**
 * Color Theme file
 * This is file has no relatios to the factory mode
 * @author leeon
 * 2013-05-02
 * */
public class Theme {

	private String color_background;
	private String color_key;
	private String color_fun_key;

	public String getColor_background() {
		return color_background;
	}

	public void setColor_background(String color_background) {
		this.color_background = color_background;
	}

	public String getColor_key() {
		return color_key;
	}

	public void setColor_key(String color_key) {
		this.color_key = color_key;
	}

	public String getColor_fun_key() {
		return color_fun_key;
	}

	public void setColor_fun_key(String color_fun_key) {
		this.color_fun_key = color_fun_key;
	}

}
